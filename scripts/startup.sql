
CREATE TABLE "usuarios" (
  "id" uuid PRIMARY KEY,
  "nombre_usuario" varchar UNIQUE NOT NULL,
  "email" varchar UNIQUE NOT NULL,
  "password_hash" varchar NOT NULL,
  "nombres" varchar NOT NULL,
  "apellidos" varchar NOT NULL,
  "fecha_nacimiento" date,
  "numero_telefono" varchar,
  "avatar_url" varchar,
  "creado_at" timestamp DEFAULT (now())
);

CREATE TABLE "refresh_tokens" (
  "id" uuid PRIMARY KEY,
  "usuario_id" uuid NOT NULL,
  "token" varchar NOT NULL UNIQUE,
  "fecha_expiracion" timestamp NOT NULL,
  "esta_revocado" boolean DEFAULT false NOT NULL,
  "creado_at" timestamp DEFAULT (now()),
  
  CONSTRAINT "fk_usuario_tokens" FOREIGN KEY ("usuario_id") 
    REFERENCES "usuarios" ("id") ON DELETE CASCADE
);

-- 1. Tabla de conversaciones (Faltaba)
CREATE TABLE "conversaciones" (
  "id" uuid PRIMARY KEY,
  "es_grupal" boolean DEFAULT false,
  "nombre" varchar, -- Solo si es grupal
  "creado_at" timestamp DEFAULT (now())
);

-- 2. Tu tabla de participantes (Perfecta, solo añadimos FK)
CREATE TABLE "participantes_conversacion" (
  "conversacion_id" uuid REFERENCES "conversaciones"("id") ON DELETE CASCADE,
  "usuario_id" uuid REFERENCES "usuarios"("id") ON DELETE CASCADE,
  "unido_at" timestamp DEFAULT (now()),
  "ultimo_leido_at" timestamp,
  PRIMARY KEY ("conversacion_id", "usuario_id")
);

CREATE TABLE "tipos_mensaje" (
  "id" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "codigo" varchar UNIQUE NOT NULL,
  "descripcion" varchar
);

-- 3. Mensajes con la FK hacia conversaciones e índices
CREATE TABLE "mensajes" (
  "id" uuid PRIMARY KEY, -- Generado por el Front
  "conversacion_id" uuid REFERENCES "conversaciones"("id") ON DELETE CASCADE,
  "emisor_id" uuid,
  "tipo_mensaje_id" int REFERENCES "tipos_mensaje"("id"),
  "contenido" text,
  "creado_at" timestamp DEFAULT (now())
);

-- Índices para que el Worker y las queries vuelen
CREATE INDEX idx_mensajes_conversacion_fecha ON "mensajes" ("conversacion_id", "creado_at" DESC);
CREATE INDEX idx_participantes_usuario ON "participantes_conversacion" ("usuario_id");

COMMENT ON COLUMN "usuarios"."id" IS 'Identificador único del usuario';

COMMENT ON COLUMN "usuarios"."avatar_url" IS 'URL de la imagen en S3';

COMMENT ON COLUMN "conversaciones"."tipo" IS 'DIRECTO o GRUPAL';

COMMENT ON COLUMN "conversaciones"."nombre" IS 'Nulo si es un chat directo';

COMMENT ON COLUMN "participantes_conversacion"."ultimo_leido_at" IS 'Para calcular mensajes no leídos';

COMMENT ON COLUMN "tipos_mensaje"."codigo" IS 'Ej: TEXTO, IMAGEN, VIDEO, AUDIO';

COMMENT ON COLUMN "tipos_mensaje"."descripcion" IS 'Descripción legible del formato';

COMMENT ON COLUMN "mensajes"."tipo_mensaje_id" IS 'Referencia al catálogo de tipos';

COMMENT ON COLUMN "mensajes"."contenido" IS 'Texto del mensaje o URL de S3';

